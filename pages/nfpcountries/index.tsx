import type { GetStaticProps, NextPage } from "next";
import Head from "next/head";
import BackToHome from "../../components/backToHome";
import Footer from "../../components/footer";
import Heading, { Headings } from "../../components/heading";
import getNfpCountries from "../../lib/data/getNfpCountries";
import styles from "../../styles/home.module.css";
import * as d3 from "d3";

type Props = {
  nfps: any;
};

const NfpCountries: NextPage<Props> = ({ nfps }) => {
  const width = 900;
  const margin = 20;
  const maxSize = 50;

  const yearRange = d3.extent(nfps.map((nfp) => nfp.year));
  const perYear = d3.group(nfps, (d) => d.year);
  const countRange = d3.extent(
    [...perYear.values()].map((countries) => countries.length)
  );

  const scale = d3
    .scaleSqrt()
    .domain(countRange)
    .range([3, maxSize / 2]);
  const scaleTime = d3
    .scaleTime()
    .domain(yearRange)
    .range([0, width - margin * 2 - maxSize * 2]);

  return (
    <>
      <Head>
        <title>NFP Countries</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
        <Heading Tag={Headings.H1}>NFP Countries</Heading>

        <p className={styles.description}>Insights into NFP Countries.</p>
        <p>
          <BackToHome />
        </p>
        <svg width={width} height={200}>
          <g transform={`translate(${margin}, ${margin})`}>
            {d3.range(yearRange[0], yearRange[1] + 1, 1).map((year) => {
              const yearData = perYear.get(year);
              const y = scaleTime(year);
              return (
                <g transform={`translate(${scaleTime(year)}, ${maxSize})`}>
                  <circle
                    cx={maxSize}
                    cy={0}
                    r={scale(perYear.get(year)?.length) ?? 1}
                    fill={yearData ? "black" : "lightgrey"}
                    fillOpacity={yearData ? 0.2 : 1}
                    stroke={yearData ? "black" : "none"}
                  />
                  <line
                    x1={maxSize}
                    y1={5}
                    x2={maxSize}
                    y2={maxSize / 2 + 5}
                    strokeWidth={0.5}
                    stroke={"grey"}
                  />
                  <text
                    fontSize={"10"}
                    textAnchor="middle"
                    x={maxSize}
                    y={maxSize / 2 + 20}
                  >
                    {year}
                  </text>
                </g>
              );
            })}
          </g>
        </svg>
      </main>

      <Footer />
    </>
  );
};

export const getStaticProps: GetStaticProps<Props> = async () => {
  const nfps = getNfpCountries();
  return {
    props: {
      nfps,
    },
  };
};

export default NfpCountries;
