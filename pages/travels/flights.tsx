export async function getStaticProps() {
  const flights = await getFlights();
  const world = await getCountries();
  return {
    props: {
      odMatrix: flights.odMatrix,
      odMatrixMJ: flights.odMatrixMJ,
      world,
    },
  };
}

export default Flights;
